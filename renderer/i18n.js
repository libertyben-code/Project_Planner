let _lang = localStorage.getItem('appLang') || 'fr';

export function t(key) {
  return (TRANSLATIONS[_lang] || TRANSLATIONS.fr)[key] ?? TRANSLATIONS.fr[key] ?? key;
}

export function getCurrentLang() { return _lang; }

export function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  _lang = lang;
  localStorage.setItem('appLang', lang);
  document.documentElement.lang = lang === 'es' ? 'es' : lang === 'en' ? 'en' : 'fr';
}

export function applyStaticI18n(root = document) {
  root.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = t(key);
    if (val && val !== key) {
      const children = [...el.childNodes].filter(n => n.nodeType === Node.ELEMENT_NODE);
      el.textContent = val;
      children.forEach(c => el.appendChild(c));
    }
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = t(el.dataset.i18nPlaceholder);
    if (val) el.placeholder = val;
  });
  root.querySelectorAll('[data-i18n-title]').forEach(el => {
    const val = t(el.dataset.i18nTitle);
    if (val) el.title = val;
  });
}
